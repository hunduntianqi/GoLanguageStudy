/*
   Go的安装:
        Go官网下载地址：https://golang.org/dl/
   配置GOPATH:
        GOPATH指一个环境变量, 用来表明Go项目的存放路径, 项目代码存放在GOPATH的src目录下
        配置路径 ==> 系统环境变量:
            我的电脑->属性->高级系统设置->环境变量: 在系统环境变量中添加GOPATH, 内容为: GO项目路径
    GOROOT: 配置Golang编译器, 路径为Golang安装路径
    GOPATH目录下存放三个文件夹:
        bin: 用来存放编译后生成的可执行文件
        pkg: 用来存放编译后生成的归档文件
        src: 用来存放源码文件
*/
